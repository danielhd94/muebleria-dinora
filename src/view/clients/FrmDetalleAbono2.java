    private void btnEliminarAbono2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarAbono2ActionPerformed
        int filaSel = dtDetalleAbono.getSelectedRow();
        if (filaSel == -1) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione un abono a eliminar");
        } else {
            int idPago = Integer.parseInt(dtDetalleAbono.getValueAt(filaSel, 0).toString());
            ControllerPayment funPago = new ControllerPayment();

            int opcion = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro que desea eliminar este Abono?", "Confirmar", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (opcion == 0) {
                if(funPago.eliminarAbono(idPago)){
                    mostrarAbonos("");
                    FrmMain.mostrar("");
                    view.FrmMain.lblSaldoActualCliente.setText(MyFormater.formato(new ControllerClient().saldoActualCliente(String.valueOf(Idcliente))));
                    JOptionPane.showMessageDialog(null,"Abono eliminado");
                }
                
            }
        }
    }//GEN-LAST:event_btnEliminarAbono2ActionPerformed

