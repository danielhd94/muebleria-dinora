    private void ingresarAbono() {
        if (!txtCantidadAbono.getText().equals("")) {
            Double limiteCredito = new ControllerClient().limiteCreditoCliente(String.valueOf(IDCLIENTE));
            Double abono = Double.parseDouble(txtCantidadAbono.getText());
            Double SALDO_ACTUAL_USUARIO = new ControllerClient().saldoActualCliente(String.valueOf(IDCLIENTE));
            if (abono > SALDO_ACTUAL_USUARIO) {
                int opcion = JOptionPane.showConfirmDialog(null, "Estas abonando $" + abono + " que es mayor que el saldo de $" + SALDO_ACTUAL_USUARIO + "\n"
                        + "¿Desea liquidar el adeudo del cliente?", "Confirmar", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (opcion == 0) {
                    liquidarCuenta();
                }
            } else {
                abonar(abono);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Verifique su monto que desea abonar");
        }
    }

    private void abonar(Double monto) {
        ModelPaymet pago = new ModelPaymet();
        ControllerPayment func = new ControllerPayment();
        pago.setIdcliente(IDCLIENTE);
        pago.setIdempleado(IDEMPLEADO);
        pago.setMonto(monto);
        if (func.insertar(pago)) {
            //ABONAR DEUDO CLIENTE
            try {
                extras.Impresora.imprimirAbonoCuentaCliente(FrmMain.confiEncabezadoTicket, FrmMain.getNombreCliente(IDCLIENTE), String.valueOf(IDCLIENTE));
                view.FrmMain.beep = false;
                FrmMain.notificar(MyFormater.formato(monto) + " Abonados al cliente");
            } catch (PrinterException ex) {
                ex.printStackTrace();
            }
            view.FrmMain.mostrar("");
            view.FrmMain.lblSaldoActualCliente.setText(MyFormater.formato(new ControllerClient().saldoActualCliente(String.valueOf(IDCLIENTE))));

            //REGISTRAR MOVIMIETO CLIENTE
            ModelMovClient dtMovC = new ModelMovClient();
            ControllerMovClient funcMovC = new ControllerMovClient();

            dtMovC.setIdcliente(IDCLIENTE);
            dtMovC.setTipo(1); //El valor es 1 cuando es ABONO
            dtMovC.setMonto(monto);
            funcMovC.insertar(dtMovC);
            dispose();
        }
    }

    private void liquidarCuenta() {
        ModelPaymet dts = new ModelPaymet();
        ControllerPayment func = new ControllerPayment();

        //LIQUIDACIÓN DE CUENTA
        try {
            extras.Impresora.imprimirLiquidacionCuentaCliente(FrmMain.confiEncabezadoTicket, FrmMain.getNombreCliente(IDCLIENTE), String.valueOf(IDCLIENTE));
        } catch (PrinterException ex) {
            ex.printStackTrace();
        }

        dts.setIdcliente(IDCLIENTE);
        new ControllerTicket().liquidarTicketCliente(IDCLIENTE);
        if (func.eliminar(dts)) {
            FrmMain.cargarMeses("" + IDCLIENTE);
        }
        FrmMain.cargarMeses("" + IDCLIENTE);
        FrmMain.lblSaldoActualCliente.setText(MyFormater.formato(new ControllerClient().saldoActualCliente(String.valueOf(IDCLIENTE))));
        vaciarListaCreditos();
        FrmMain.mostrar(""); //PARA ACTUALIZAR SALDO ACTUAL EN EL LISTADO DE CLIENTES
        FrmMain.btnAbonarCliente.setEnabled(false);
        FrmMain.btnLiquidarAdeudo.setEnabled(false);
        dispose();
    }

