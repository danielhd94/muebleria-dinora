    private void txtCantidadAbonoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadAbonoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtCantidadAbono.getText().equals("") || txtCantidadAbono.getText().equals("0.00") || txtCantidadAbono.getText().equals("0")) {
                view.FrmMain.notificar("No es posible abonar " + txtCantidadAbono.getText());
            } else {
                ingresarAbono();
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_ESCAPE) {
            dispose();
        }
    }//GEN-LAST:event_txtCantidadAbonoKeyPressed

