    private void ingresar(String user, String password) {
        System.out.println("INGRESANDO");
        try {
            ControllerEmploye func = new ControllerEmploye();
            boolean band = func.validar(user, password);

            if (band) {
                System.out.println("BANDERA");
                empleado = func.getWorker(user, password);
                if (empleado.getInactivo() == 1) {
                    JOptionPane.showMessageDialog(this, "El trabajador ya no pertenece a la empresa", "Información", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                lblCargando.setVisible(true);
                progressBar.setVisible(true);
                task.go();
                timer.start();
                System.out.println("INICIANDO...");
            } else {
                lblCargando.setVisible(true);
                boolean r = new ControllerEmploye().perteneceAdministrador(user);
                if (r) {
                    lblCargando.setText("La cuenta o la contraseña es incorrecta.\nSi no recuerdas la cuenta.");
                    lblRestablecerAhora.setVisible(true);
                } else {
                    lblRestablecerAhora.setVisible(false);
                    lblCargando.setText("La cuenta o la contraseña es incorrecta.");
                }
            }
        } catch (Exception e) {
        }
    }

    private void iniciar() {
        finicio = new FrmMain();
        finicio.IdempleadoU = empleado.getIdempleado();
        finicio.btnUsuario.setText(empleado.getNombre());
        finicio.setVisible(true);
        //MENSAJE DE BIENVENIDA
        finicio.beep = false;
        finicio.notificar("Bienvenido " + empleado.getNombre());

        if (empleado.getPrivilegio() != 1) {
            finicio.btnClientesH.setEnabled(false);
            finicio.btnProductosH.setEnabled(false);
            finicio.btnInventarioH.setEnabled(false);
            finicio.btnConfiguracionH.setEnabled(false);
            finicio.btnCorte.setEnabled(false);
        }
        timer.stop();
        dispose();
    }


