    class TimerListener implements ActionListener {

        public void actionPerformed(ActionEvent evt) {
            progressBar.setValue(task.getCurrent());
            m++;
            if (m == 1) {
                lblCargando.setText("Cargando.");
            }
            if (m == 2) {
                lblCargando.setText("Cargando..");
            }
            if (m == 3) {
                lblCargando.setText("Cargando...");
                m = 0;
            }

            if (task.done()) {
                //listo = true;
                lblCargando.setText("¡Listo!");
                iniciar();
            }
        }
    }

    private void login() {
        if (this.txtUsuario.getText().equals("") && this.txtPassword.getText().equals("")) {
            //JOptionPane.showMessageDialog(null, "Ingrese contraseña", "Acceso al sistema", JOptionPane.ERROR_MESSAGE);
            lblCargando.setVisible(true);
            lblCargando.setText("No tiene acceso al sistema.");
            this.txtUsuario.requestFocus();
        } else {
            if (this.txtUsuario.getText().equals("")) {
                //JOptionPane.showMessageDialog(null, "Ingrese contraseña", "Acceso al sistema", JOptionPane.ERROR_MESSAGE);
                lblCargando.setVisible(true);
                lblCargando.setText("Ingrese usuario o email");
                this.txtUsuario.requestFocus();
            } else {
                if (this.txtPassword.getText().equals("")) {
                    //JOptionPane.showMessageDialog(null, "Ingrese contraseña", "Acceso al sistema", JOptionPane.ERROR_MESSAGE);
                    lblCargando.setVisible(true);
                    lblCargando.setText("Ingrese contraseña");
                    this.txtPassword.requestFocus();
                } else {
                    ingresar(this.txtUsuario.getText(), this.txtPassword.getText());
                }
            }

        }
    }

