    int seleccionarFila = 0;
    private void dtBuscarProductoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_dtBuscarProductoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_KP_UP) {
            seleccionarFila++;
            dtBuscarProducto.changeSelection(seleccionarFila, 1, false, false);
        }
        if (evt.getKeyCode() == KeyEvent.VK_KP_DOWN) {
            seleccionarFila--;
            dtBuscarProducto.changeSelection(seleccionarFila, 1, false, false);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int fila = dtBuscarProducto.getSelectedRow();
            String noserie = dtBuscarProducto.getValueAt(fila, 1).toString();
            enterProducto(noserie, "1");
            dispose();
        }
    }//GEN-LAST:event_dtBuscarProductoKeyPressed

