    public static void mostrarPro(String buscar) {
        try {
            DefaultTableModel modelo;
            ControllerProduct func = new ControllerProduct();
            modelo = func.mostrarBusqueda(buscar);

            dtBuscarProducto.setModel(modelo);
            ocultar_columnasPro();
            if (dtBuscarProducto.getRowCount() > 0) {
                dtBuscarProducto.changeSelection(0, 1, false, false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    static void ocultar_columnasPro() {
        for (int i = 0; i < dtBuscarProducto.getColumnCount(); i++) {
            if (i != 2 && i != 4 && i != 6) {
                dtBuscarProducto.getColumnModel().getColumn(i).setMaxWidth(1);
                dtBuscarProducto.getColumnModel().getColumn(i).setMinWidth(1);
                dtBuscarProducto.getColumnModel().getColumn(i).setPreferredWidth(1);
            }
        }

    }

