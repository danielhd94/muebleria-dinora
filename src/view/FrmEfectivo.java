    public static void evaluarMontoPago() {
        double cambio = pagoCon() - importeTotal();
        if (pagoCon() < importeTotal()) {
            lblError.setText("El efectivo ingresado es menor que total a cobrar. Verifique");
            lblCambio.setText("$0.00");
            cobrar = false;
            view.FrmMain.displayMessage(pagoCon() + " - " + importeTotal() + " = " + cambio);
        } else {
            cobrar = true;
            lblError.setText("");
            lblCambio.setText(MyFormater.formato(cambio));
            view.FrmMain.displayMessage(pagoCon() + " - " + importeTotal() + " = " + cambio);
        }

    }

    public static Double pagoCon() {
        Double pagocon = 0d;
        if (txtPagoCon.getText().equals("$") || txtPagoCon.getText().equals("")) {
            
        }else{
            String pago = txtPagoCon.getText();
            pago = pago.replace("$", "");
            pago = pago.replace(",", "");
            pagocon = Double.parseDouble(pago);
        }
        return pagocon;
    }

    public static Double importeTotal() {
        String imp = MyFormater.formato(FrmCobrar2.importeM);
        imp = imp.replace("$", "");
        imp = imp.replace(",", "");
        return Double.parseDouble(imp);
    }
